function flowgraphs(str)
{
   var lstr = new String(str);
   var i;
   var element;
   if ((CurrentExtension == "sfg.png") || 
       (CurrentExtension == "dfg.png") ||
       (CurrentExtension == "lfg.png")) 
   {
     for(i=0; i<Numfiles; i++)
     {
        element = document.all.item(FlowIdArr[i]);
        if (lstr.indexOf(WorkStemArr[i]) != -1)
        {
           set_fg_links(element.all,CurrentExtension,0);
           element.style.display = ""; 
        }
        else 
        {
           element.style.display = "none"; 
        }
     }
   }
}
